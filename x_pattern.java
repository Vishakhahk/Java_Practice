package pattern;

public class x_pattern {

	public static void main(String[] args) 
	{
		
	
	for(int r = 4; r >= 1; r--) {
        
        
        for (int s = 2; s >= r; s--) {
            System.out.print(" ");
        }
    
        for (int c = 1; c <= r; c++) {
        	System.out.print( "@ ");
   
       
    }
        System.out.println();
   
}
for(int r = 2; r <= 4; r++) {
        
        
        for (int s = 2; s >= r; s--) {
            System.out.print(" ");
        }
    
        for (int c = 1; c <= r; c++) {
        	System.out.print( "@ ");
       
   
       
    }
        System.out.println();
   
}
	}
}
